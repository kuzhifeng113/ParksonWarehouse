package com.woyun.warehouse.bean;

public class UnReadNumBean {

    /**
     * sysUnreadNum : 10
     * billUnreadNum : 0
     */

    private int sysUnreadNum;
    private int billUnreadNum;

    public int getSysUnreadNum() {
        return sysUnreadNum;
    }

    public void setSysUnreadNum(int sysUnreadNum) {
        this.sysUnreadNum = sysUnreadNum;
    }

    public int getBillUnreadNum() {
        return billUnreadNum;
    }

    public void setBillUnreadNum(int billUnreadNum) {
        this.billUnreadNum = billUnreadNum;
    }
}
